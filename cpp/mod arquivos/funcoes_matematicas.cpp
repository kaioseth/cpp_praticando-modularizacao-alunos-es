#include <cmath>
#include "funcoes_matematicas.h"

double potencia(double base, double expoente) {
    return pow(base, expoente);
}

double raiz(double numero) {
    return sqrt(numero);
}

double seno(double angulo) {
    return sin(angulo);
}

double cosseno(double angulo) {
    return cos(angulo);
}

double tangente(double angulo) {
    return tan(angulo);
}

double logaritmo(double numero) {
    return log(numero);
}
